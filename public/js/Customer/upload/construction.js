AOS.init({
    duration: 550,
});


let btn1 = document.getElementById("btn1");
window.onscroll = function () {
    if (scrollY >= 200) {
        btn1.style.display = "flex";
    } else {
        btn1.style.display = "none";

    }
}
btn1.addEventListener("click", function () {
    scroll({
        top: 0,
        left: 0,
        behavior: "smooth"
    })
});

